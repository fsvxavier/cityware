    /**
     * Checks to see if a string is utf8 encoded.
     *
     * @param  string $Str The string to be checked
     * @return bool   True if $Str fits a UTF-8 model, false otherwise.
     */
    public static function seemsUtf8($Str) { # by bmorel at ssi dot fr
        $length = strlen($Str);
        for ($i = 0; $i < $length; $i++) {
            if (ord($Str[$i]) < 0x80)
                continue;# 0bbbbbbb
            elseif ((ord($Str[$i]) & 0xE0) == 0xC0)
                $n = 1;# 110bbbbb
            elseif ((ord($Str[$i]) & 0xF0) == 0xE0)
                $n = 2;# 1110bbbb
            elseif ((ord($Str[$i]) & 0xF8) == 0xF0)
                $n = 3;# 11110bbb
            elseif ((ord($Str[$i]) & 0xFC) == 0xF8)
                $n = 4;# 111110bb
            elseif ((ord($Str[$i]) & 0xFE) == 0xFC)
                $n = 5;# 1111110b
            else
                return false;# Does not match any model
            for ($j = 0; $j < $n; $j++) { # n bytes matching 10bbbbbb follow ?
                if (( ++$i == $length) || ((ord($Str[$i]) & 0xC0) != 0x80))
                    return false;
            }
        }

        return true;
    }

    public static function utf8UriEncode($utf8_string, $length = 0) {
        $unicode = '';
        $values = array();
        $num_octets = 1;
        $unicode_length = 0;
        $string_length = strlen($utf8_string);
        for ($i = 0; $i < $string_length; $i++) {
            $value = ord($utf8_string[$i]);
            if ($value < 128) {
                if ($length && ($unicode_length >= $length))
                    break;
                $unicode .= chr($value);
                $unicode_length++;
            } else {
                if (count($values) == 0)
                    $num_octets = ($value < 224) ? 2 : 3;
                $values[] = $value;
                if ($length && ($unicode_length + ($num_octets * 3)) > $length)
                    break;
                if (count($values) == $num_octets) {
                    if ($num_octets == 3) {
                        $unicode .= '%' . dechex($values[0]) . '%' . dechex($values[1]) . '%' . dechex($values[2]);
                        $unicode_length += 9;
                    } else {
                        $unicode .= '%' . dechex($values[0]) . '%' . dechex($values[1]);
                        $unicode_length += 6;
                    }
                    $values = array();
                    $num_octets = 1;
                }
            }
        }

        return $unicode;
    }

    /**
     * Sanitizes title, replacing whitespace with dashes.
     *
     * Limits the output to alphanumeric characters, underscore (_) and dash (-).
     * Whitespace becomes a dash.
     *
     * @param  string $title The title to be sanitized.
     * @return string The sanitized title.
     */
    public static function slugify($title, $length = 200) {
        $title = strip_tags($title);
        // Preserve escaped octets.
        $title = preg_replace('|%([a-fA-F0-9][a-fA-F0-9])|', '---$1---', $title);
        // Remove percent signs that are not part of an octet.
        $title = str_replace('%', '', $title);
        // Restore octets.
        $title = preg_replace('|---([a-fA-F0-9][a-fA-F0-9])---|', '%$1', $title);
        $title = self::removeAccents($title);
        if (self::seemsUtf8($title)) {
            if (function_exists('mb_strtolower')) {
                $title = mb_strtolower($title, 'UTF-8');
            }
            $title = self::utf8UriEncode($title, $length);
        }
        $title = strtolower($title);
        $title = preg_replace('/&.+?;/', '', $title); // kill entities
        $title = preg_replace('/[^%a-z0-9 _-]/', '', $title);
        $title = preg_replace('/\s+/', '-', $title);
        $title = preg_replace('|-+|', '-', $title);
        $title = trim($title, '-');

        return $title;
    }

    /**
     * Função de tratamento de string para formato de URL Amigavel (SLUG)
     * @param  string $string
     * @param  string $separator
     * @return string
     */
    public static function slug($string, $separator = '-') {
        $q_separator = preg_quote($separator, '#');
        $code = array(
            '&.+?;' => '',
            '[^a-z0-9 _-]' => '',
            '\s+' => $separator,
            '(' . $q_separator . ')+' => $separator
        );

        $string = strip_tags($string);
        foreach ($code as $key => $val) {
            $string = preg_replace('#' . $key . '#i', $val, $string);
        }

        $string = strtolower($string);

        return trim($string);
    }

